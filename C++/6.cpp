#include<iostream>
using namespace std;
int main()
	{
		int num=55;
		if (num>=75)
			{
			cout<<"Grade is A"<<endl;
			}
		else if (num>=65)
			{
			cout<<"Grade is B"<<endl;
			}
		else if (num>=55)
			{
			cout<<"Grade is C"<<endl;
			}
		else
			{
			cout<<"Grade is S"<<endl;
			}
		return 0;
	}
